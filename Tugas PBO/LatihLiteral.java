public class LatihLiteral {
    public static void main(String[] args) {
        byte nilaiMaksimumByte = 127;
        short tahunSaatIni = 2026;
        int populasiIndonesia = 275000000;
        long populasiDunia = 8000000000L;
        float pi = 3.14F;
        double e = 2.718281828;
        char hurufPertama = 'E';
        boolean status = true;

        System.out.println("=== DEMONSTRASI LITERAL TIPE DATA ===");
        System.out.println("Tipe byte           : " + nilaiMaksimumByte);
        System.out.println("Tipe short          : " + tahunSaatIni);
        System.out.println("Tipe int            : " + populasiIndonesia);
        System.out.println("Tipe long           : " + populasiDunia);
        System.out.println("Tipe float          : " + pi);
        System.out.println("Tipe double         : " + e);
        System.out.println("Tipe hurufPertama   : " + hurufPertama);
        System.out.println("Tipe status         : " + status);
    }
}